    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
String nama = jTextField3.getText();
String jenisKelamin = jComboBox2.getSelectedItem().toString();
String nik = jTextField7.getText();
String noHp = jTextField11.getText();
String alamat = jTextField12.getText();

// Cek apakah jenis kelamin sudah dipilih
if (jenisKelamin.equalsIgnoreCase("Pilih")) {
    JOptionPane.showMessageDialog(this, "Jenis Kelamin belum dipilih", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

// Cek apakah semua field telah diisi
if (nama.isEmpty() || nik.isEmpty() || noHp.isEmpty() || alamat.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

// Cek apakah nomor HP (noHp) hanya berisi angka dan memiliki panjang antara 10 hingga 13 digit
if (!noHp.matches("\\d{10,13}")) {
    JOptionPane.showMessageDialog(this, "Nomor HP harus terdiri dari 10 hingga 13 digit angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

// Cek apakah alamat (jTextField12) hanya terdiri dari huruf dan angka (kombinasi string dan integer diperbolehkan)
if (!alamat.matches("[a-zA-Z0-9\\s]+")) {
    JOptionPane.showMessageDialog(this, "Alamat hanya boleh berisi huruf, angka, dan spasi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

TodoRepo repo = new TodoRepo();
int result = repo.addMemberToDatabase(nama, jenisKelamin, nik, noHp, alamat);

if (result <= 0) {
    JOptionPane.showMessageDialog(this, "Gagal menyimpan data ke database!", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

// Tambahkan data ke dalam model tabel
DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
int nomorUrut = model.getRowCount() + 1;
model.addRow(new Object[]{nomorUrut, nama, jenisKelamin, nik, noHp, alamat});

jumlahAnggota = model.getRowCount();  // Update jumlahAnggota after adding

// Set renderer untuk semua kolom agar data rata kiri
DefaultTableCellRenderer leftRenderer = new DefaultTableCellRenderer();
leftRenderer.setHorizontalAlignment(SwingConstants.LEFT);

for (int i = 0; i < jTable1.getColumnCount(); i++) {
    jTable1.getColumnModel().getColumn(i).setCellRenderer(leftRenderer);
}

JOptionPane.showMessageDialog(this, "Anggota berhasil didaftarkan", "Berhasil", JOptionPane.INFORMATION_MESSAGE);

// Reset input
jTextField3.setText("");
jTextField7.setText("");
jTextField11.setText("");
jTextField12.setText("");
jComboBox2.setSelectedIndex(0);
    }//GEN-LAST:event_jButton3ActionPerformed

