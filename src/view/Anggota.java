    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
           String nikInput = jTextField8.getText().trim();
    if (nikInput.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan NIK yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data dengan NIK: " + nikInput + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        TodoRepo repo = new TodoRepo();
        int result = repo.deleteMemberFromDatabase(nikInput);

        if (result > 0) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            boolean found = false;

            for (int i = 0; i < model.getRowCount(); i++) {
                String tableNik = model.getValueAt(i, 3).toString().trim();
                if (tableNik.equals(nikInput)) {
                    model.removeRow(i);
                    found = true;
                    break;
                }
            }
            if (found) {
                jumlahAnggota = model.getRowCount();  // Update jumlahAnggota after deletion  
                JOptionPane.showMessageDialog(this, "Anggota telah dihapus dari data", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Anggota tidak ditemukan di tabel.", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "NIK tidak ditemukan di database.", "Error", JOptionPane.ERROR_MESSAGE);
        }}
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
      String nikInput = jTextField8.getText().trim(); // Ambil input dari JTextField8
     // Cek apakah input kosong
     if (nikInput.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Masukkan NIK yang ingin dicari.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

// Dapatkan model asli dari JTable
DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

// Buat TableRowSorter untuk menyaring data di JTable
TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
jTable1.setRowSorter(sorter);

// Filter berdasarkan NIK (kolom ke-4, indeks ke-3)
RowFilter<DefaultTableModel, Object> rf = RowFilter.regexFilter("^" + nikInput + "$", 3); // Indeks 3 untuk kolom ke-4 (NIK)
sorter.setRowFilter(rf);

// Cek apakah ada hasil yang ditampilkan setelah filter
if (sorter.getViewRowCount() == 0) {
    JOptionPane.showMessageDialog(this, "NIK tidak ditemukan di tabel.", "Info", JOptionPane.INFORMATION_MESSAGE);
    // Reset filter agar menampilkan semua data kembali
    sorter.setRowFilter(null);
    return;
}

// Jika ditemukan, ambil indeks baris yang sesuai dengan filter
int modelRowIndex = sorter.convertRowIndexToModel(0); // Ambil baris pertama yang cocok

// Ambil data dari baris yang cocok
String nama = model.getValueAt(modelRowIndex, 1).toString(); // Kolom Nama
String jenisKelamin = model.getValueAt(modelRowIndex, 2).toString(); // Kolom Jenis Kelamin
String noHp = model.getValueAt(modelRowIndex, 4).toString(); // Kolom No HP
String alamat = model.getValueAt(modelRowIndex, 5).toString(); // Kolom Alamat

// Tampilkan dialog untuk mengedit data
String newNama = JOptionPane.showInputDialog(this, "Edit Nama:", nama);

// Gunakan JComboBox untuk pilihan jenis kelamin
JComboBox<String> genderComboBox = new JComboBox<>(new String[]{"Male", "Female"});
genderComboBox.setSelectedItem(jenisKelamin); // Set nilai awal sesuai data lama

int genderOption = JOptionPane.showConfirmDialog(
    this,
    genderComboBox,
    "Edit Jenis Kelamin:",
    JOptionPane.OK_CANCEL_OPTION,
    JOptionPane.PLAIN_MESSAGE
);

// Ambil nilai yang dipilih dari JComboBox jika pengguna menekan OK
String newJenisKelamin = (genderOption == JOptionPane.OK_OPTION) ? (String) genderComboBox.getSelectedItem() : null;

String newNoHp = JOptionPane.showInputDialog(this, "Edit No HP:", noHp);
String newAlamat = JOptionPane.showInputDialog(this, "Edit Alamat:", alamat);

// Update data di JTable jika pengguna tidak membatalkan input
if (newNama != null && newJenisKelamin != null && !newJenisKelamin.equals("Pilih") && newNoHp != null && newAlamat != null) {
    model.setValueAt(newNama, modelRowIndex, 1);           // Update kolom Nama
    model.setValueAt(newJenisKelamin, modelRowIndex, 2);   // Update kolom Jenis Kelamin
    model.setValueAt(newNoHp, modelRowIndex, 4);           // Update kolom No HP
    model.setValueAt(newAlamat, modelRowIndex, 5);         // Update kolom Alamat

    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui.", "Info", JOptionPane.INFORMATION_MESSAGE);
} else {
    JOptionPane.showMessageDialog(this, "Edit dibatalkan atau jenis kelamin tidak valid.", "Peringatan", JOptionPane.WARNING_MESSAGE);
}

// Reset filter untuk menampilkan semua data kembali
sorter.setRowFilter(null);


    }//GEN-LAST:event_jButton8ActionPerformed

