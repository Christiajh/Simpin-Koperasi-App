    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    // Ambil input dari JTextField dan hapus spasi di depan/belakang
String idAnggotaInput = jTextField14.getText().trim();

if (idAnggotaInput.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Masukkan ID anggota yang ingin dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

// Pastikan ID Anggota ada di database
TodoRepo repo = new TodoRepo();
int checkResult = repo.hapuspinjaman(idAnggotaInput);  // Panggil fungsi hapus untuk cek keberadaan ID

if (checkResult == 1) {
    // Jika data ditemukan dan berhasil dihapus di database, hapus di JTable
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    boolean found = false;

    // Cari ID anggota di kolom pertama (indeks kolom ke-0)
    for (int i = 0; i < model.getRowCount(); i++) {
        // Pastikan data diambil secara aman dan tidak null
        String tableId = model.getValueAt(i, 0) != null ? model.getValueAt(i, 0).toString().trim() : "";
        if (tableId.equals(idAnggotaInput)) {
            model.removeRow(i);  // Hapus baris dari tabel
            found = true;
            break;
        }
    }

    if (found) {
        JOptionPane.showMessageDialog(this, "Data Pinjaman telah dihapus.", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Data Pinjaman tidak ditemukan di tabel.", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
} else {
    JOptionPane.showMessageDialog(this, "ID Pinjaman anggota tidak ditemukan di database.", "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_jButton7ActionPerformed

