    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       String sql = """
            SELECT namaAnggota, 
                   SUM(jumlah) AS totalPinjaman
            FROM pinjaman
            GROUP BY idAnggota, namaAnggota
            ORDER BY namaAnggota ASC
        """;

try (Connection connection = (Connection) DBUtil.getConnection();
     PreparedStatement stmt = connection.prepareStatement(sql);
     ResultSet rs = stmt.executeQuery()) {

    StringBuilder messageBuilder = new StringBuilder("Daftar Pinjaman Anggota:\n\n");
    double totalKeseluruhan = 0;  // Variabel untuk menghitung total pinjaman keseluruhan

    // Mengolah hasil query
    while (rs.next()) {
        String nama = rs.getString("namaAnggota");  // Mengambil nama anggota
        double totalPinjaman = rs.getDouble("totalPinjaman");  // Mengambil total pinjaman anggota

        // Menggunakan waktu saat ini sebagai waktu peminjaman
        LocalDateTime waktuPeminjaman = LocalDateTime.now();
        LocalDate tanggalPeminjaman = waktuPeminjaman.toLocalDate();

        // Format waktu peminjaman ke HH:mm:ss
        String waktuFormatted = waktuPeminjaman.format(DateTimeFormatter.ofPattern("HH:mm:ss"));

        // Mengambil nama hari dalam bahasa Indonesia berdasarkan waktu saat ini
        String namaHari = tanggalPeminjaman.getDayOfWeek().getDisplayName(TextStyle.FULL, new Locale("id", "ID"));

        // Menambahkan informasi pinjaman ke dalam message
        messageBuilder.append("Nama: ").append(nama).append("\n")
                      .append("Total Pinjaman: Rp ").append(String.format("%.2f", totalPinjaman)).append("\n")
                      .append("Waktu Peminjaman: ").append(tanggalPeminjaman.format(DateTimeFormatter.ofPattern("dd-MM-yyyy")))
                      .append(" ").append(waktuFormatted)
                      .append(" (").append(namaHari).append(")\n\n");

        // Menambahkan total pinjaman ke total keseluruhan
        totalKeseluruhan += totalPinjaman;
    }

    // Jika tidak ada pinjaman yang terdaftar
    if (messageBuilder.toString().equals("Daftar Pinjaman Anggota:\n\n")) {
        JOptionPane.showMessageDialog(null, "Tidak ada pinjaman yang terdaftar.", "Info", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Menambahkan informasi total keseluruhan pinjaman
        messageBuilder.append("Jumlah Total Semua Pinjaman: Rp ").append(String.format("%.2f", totalKeseluruhan)).append("\n");

        // Menampilkan pesan dalam bentuk card message
        JOptionPane.showMessageDialog(null, messageBuilder.toString(), "Informasi Pinjaman Anggota", JOptionPane.INFORMATION_MESSAGE);
    }

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengambil data.", "Error", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace();
}

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
      String outputFile = "D:\\Data_Pinjaman.pdf";

// Query untuk mengambil data pinjaman
String sql = "SELECT * FROM pinjaman";

try (Connection connection = (Connection) DBUtil.getConnection();
     PreparedStatement stmt = connection.prepareStatement(sql);
     ResultSet rs = stmt.executeQuery()) {

    // Membuat dokumen PDF
    Document document = new Document();
    PdfWriter.getInstance(document, new FileOutputStream(outputFile));

    // Membuka dokumen untuk menulis
    document.open();

    // Menulis judul dokumen dengan bold
    Font boldFont = new Font(Font.FontFamily.HELVETICA, 18, Font.BOLD);
    document.add(new Paragraph("Daftar Pinjaman Anggota", boldFont));
    document.add(new Paragraph("========================================"));

    // Menulis data pinjaman
    while (rs.next()) {
        int idAnggota = rs.getInt("idAnggota");
        String namaAnggota = rs.getString("namaAnggota");
        String jenisPinjaman = rs.getString("jenisPinjaman");
        int jumlah = rs.getInt("jumlah");
        int noRek = rs.getInt("no_rek");
        int tanggal = rs.getInt("tanggal");
        int bulan = rs.getInt("bulan");
        int tahun = rs.getInt("tahun");
        int jam = rs.getInt("jam");
        int menit = rs.getInt("menit");
        int detik = rs.getInt("detik");

        // Format tanggal dan waktu
        String formattedDate = String.format("%02d-%02d-%d", tanggal, bulan, tahun);
        String formattedTime = String.format("%02d:%02d:%02d", jam, menit, detik);

        // Menambahkan data pinjaman ke dalam dokumen PDF
        document.add(new Paragraph("ID Anggota: " + idAnggota));
        document.add(new Paragraph("Nama Anggota: " + namaAnggota));
        document.add(new Paragraph("Nomor Rekening: " + noRek));
        document.add(new Paragraph("Jenis Pinjaman: " + jenisPinjaman));
        document.add(new Paragraph("Jumlah Pinjaman: " + jumlah));
        document.add(new Paragraph("Tanggal: " + formattedDate));
        document.add(new Paragraph("Waktu: " + formattedTime));
        document.add(new Paragraph("\n----------------------------------------\n"));
    }

    // Menutup dokumen PDF
    document.close();

    // Memberikan informasi kepada pengguna bahwa PDF telah dibuat
    JOptionPane.showMessageDialog(null, "Data pinjaman berhasil diekspor ke PDF: " + outputFile,
                                  "Berhasil", JOptionPane.INFORMATION_MESSAGE);

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengambil data.", "Error", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace();
} catch (FileNotFoundException e) {
    JOptionPane.showMessageDialog(null, "File tidak ditemukan atau tidak dapat disimpan.", "Error", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace();
} catch (DocumentException ex) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat membuat file PDF.", "Error", JOptionPane.ERROR_MESSAGE);
    ex.printStackTrace();
}

    }//GEN-LAST:event_jButton10ActionPerformed

